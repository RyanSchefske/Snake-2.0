public class Position {
  public int x;
  public int y;
  public int xf;
  public int yf;
  public int xb;
  public int yb;
  public int xb2;
  public int yb2;
  public int xb3;
  public int yb3;


  public Position(int x, int y) {
    this.x = x;
    this.y = y;
  }

  public void ChangeData(int x, int y) {
    this.x = x;
    this.y = y;
  }

  public int getX() {
    return x;
  }

  public int getY() {
    return y;
  }

  public int getXf() {
    return xf;
  }

  public int getYf() {
    return yf;
  }

  public int getXb() {
    return xb;
  }

  public int getYb() {
    return yb;
  }

  public int getXb2() {
    return xb2;
  }

  public int getYb2() {
    return yb2;
  }

  public int getXb3() {
    return xb3;
  }

  public int getYb3() {
    return yb3;
  }

}
